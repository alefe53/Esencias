export class Banda {
	constructor(
		id,
		nombre,
		integrantes,
		descripcion,
		imagen,
		lanzamientos,
		imagenesextra,
	) {
		this.id = id;
		this.nombre = nombre;
		this.integrantes = integrantes;
		this.descripcion = descripcion;
		this.imagen = imagen;
		this.lanzamientos = lanzamientos;
		this.imagenesextra = imagenesextra;
	}
}
