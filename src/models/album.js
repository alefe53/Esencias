export class Album {
	constructor(
		id,
		nombre,
		participes,
		descripcion,
		portada,
		anio,
		youtubeId,
		temas,
	) {
		this.id = id;
		this.nombre = nombre;
		this.participes = participes;
		this.descripcion = descripcion;
		this.portada = portada;
		this.anio = anio;
		this.youtubeId = youtubeId;
		this.temas = temas;
	}
}
